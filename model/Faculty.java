package model;


public class Faculty {
    int id;
    String fac_name;
    String fac_id;
    String fac_email;
    String fac_pass;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFac_name() {
        return fac_name;
    }

    public void setFac_name(String fac_name) {
        this.fac_name = fac_name;
    }

    public String getFac_id() {
        return fac_id;
    }

    public void setFac_id(String fac_id) {
        this.fac_id = fac_id;
    }

    public String getFac_email() {
        return fac_email;
    }

    public void setFac_email(String fac_email) {
        this.fac_email = fac_email;
    }

    public String getFac_pass() {
        return fac_pass;
    }

    public void setFac_pass(String fac_pass) {
        this.fac_pass = fac_pass;
    }
}
